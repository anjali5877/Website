  document.querySelector(".hero-right .btn-secondary").innerText = t.whatsapp;
  document.querySelector(".hero-right small").innerText = t.privacyText;

  // Why Choose
  document.querySelector(".why-choose h2").innerText = t.whyChooseTitle;
  const whyCards = document.querySelectorAll(".why-choose .feature-card");
  whyCards.forEach((card, i) => {
    card.querySelector("h3").innerText = t.whyFeatures[i].title;
    card.querySelector("p").innerText = t.whyFeatures[i].desc;
  });

  // Loan Calculator
  document.querySelector(".loan-title").innerText = t.loanCalculator;
