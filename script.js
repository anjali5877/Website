  const t = translations[lang];

  // Hero Section
  document.querySelector(".hero .kicker").innerText = t.heroKicker;
  document.querySelector(".hero h1").innerText = t.heroTitle;
  document.querySelector(".hero h2").innerText = t.heroSubTitle;
  document.querySelector(".hero p").innerText = t.heroDescription;

  const badgeSpans = document.querySelectorAll(".hero .badges span");
  badgeSpans.forEach((span, i) => {
    span.innerText = t.badges[i];
  });

  const featureCards = document.querySelectorAll(".hero .feature-card");
  featureCards.forEach((card, i) => {
    card.querySelector("h3").innerText = t.features[i].title;
    card.querySelector("p").innerText = t.features[i].description;
  });

